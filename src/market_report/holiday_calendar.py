"""증시 휴장 달력 — 한국(KRX)·미국(NYSE/NASDAQ) 휴장일 + 사유(휴일명).

사용자 지시(2026-06-19): 휴장일엔 "무슨 날 때문에 휴장"인지 사유를 명시한 안내를
장전 리포트 시각에 발송한다(한국·미국 모두). 사유(이름)를 쓰려면 휴일명이 필요한데
시스템은 데이터 신선도로 '휴장 여부'만 알 뿐 이름은 모른다 → 이름 테이블을 둔다.

방침: 외부 API/의존성 없이 **하드코딩 테이블**(사용자 "달력만둬, 매년 한번만 세팅").
매년 초 아래 두 dict를 갱신한다. 순수 모듈(외부 import 없음, 결정론 테스트).

판정 한계: 임시공휴일·선거일 등 사전 미등재 휴장은 이 테이블이 모른다. 그러나 정규
리포트(pre/post·us_morning)는 별도 '데이터 신선도' 스킵을 이미 보유하므로, 미등재
휴일이어도 잘못된 발송은 막힌다(다만 그날 휴장 '안내'만 누락). False positive(개장일을
휴장으로 오등재)는 피해야 하므로 확실한 날짜만 등재한다.
"""
