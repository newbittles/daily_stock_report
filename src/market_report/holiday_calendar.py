def us_holiday_name(d: date | None = None) -> str | None:
    """미국 증시 휴장 사유. 휴장이 아니면 None.

    d=None이면 **미국 동부(ET) 기준 오늘** 날짜로 판정한다(KST 저녁에 호출돼도
    미국 현지 날짜로 정확히 매칭하기 위함 — us_premarket은 KST 19:00 = ET 당일 오전).
    """
    if d is None:
        d = datetime.now(ZoneInfo("America/New_York")).date()
    return US_MARKET_HOLIDAYS.get(d.isoformat())


def kr_holiday_name(d: date | None = None) -> str | None:
    """한국 증시 휴장 사유. 휴장이 아니면 None. d=None이면 KST 오늘."""
    if d is None:
        d = datetime.now(ZoneInfo("Asia/Seoul")).date()
    return KR_MARKET_HOLIDAYS.get(d.isoformat())
